
public class ClassPeriod {
	int room;
	int classSize;
	String teacher;
	
	public ClassPeriod(int room, int classSize, String teacher) {
		this.room = room;
		this.classSize = classSize;
		this.teacher = teacher;
	}
	
	public int getRoom() {
		return room;
	}
	
	public int getClassSize() {
		return classSize;
	}
	
	public String getTeacher() {
		return teacher;
	}
}
